class Solution{
  public:
    vector<int> duplicates(int arr[], int n) {
        // code here
        vector<int> res; unordered_map<int,int> hash;
        for(int i=0;i<n;i++) hash[arr[i]]++;
        for(auto it:hash) if(it.second>1) res.push_back(it.first);
        sort(res.begin(),res.end());
        if(res.size()) return res;
        return {-1};
    }
};


